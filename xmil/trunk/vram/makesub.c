typedef union {
	UINT32	d;
	UINT8	b[4];
} PAT32;

void makesub_initialize(void) {

	UINT	i;
	REG8	j;
	PAT32	pat;

	for (i=0; i<256; i++) {
		for (j=0; j<4; j++) {
			pat.b[j] = (i & (0x80 >> j))?1:0;
		}
		to256col[i*2+0] = pat.d;
		for (j=0; j<4; j++) {
			pat.b[j] = (i & (0x08 >> j))?1:0;
		}
		to256col[i*2+1] = pat.d;
	}
}

